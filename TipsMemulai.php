            <h1>Cara Membuat Air Nutrisi Hidroponik Sederhana</h1>
            <p>
                Bagi Anda yang ingin menanam buah, belilah AB MIX yang terbuat dari dua larutan cair terpisah. Cara membuat air nutrisi hidroponik AB MIX, sebagai berikut:
                <br><br>
                > Air 1 liter
                <br><br>
                > Larutan A 5 ml
                <br><br>
                > Larutan B 5 ml
                <br><br>
                > Campurkan ketiganya menjadi satu, aduk hingga merata sempurna. Larutan air nutrisi Anda siap digunakan.
            </p> <br> <br>
            <h1>Tips Bercocok Tanam Hidroponik</h1>
            <p> <br>
            1. Pastikan tanaman Anda terkena sinar matahari yang cukup, supaya tidak mudah layu akibat kekurangan fotosintesis.
            <br> <br>
            2. Hidroponik botol dalam rumah, bisa Anda letakkan dengan jendela tembus cahaya atau disiasati menggunakan lampu fotosintesis.
            <br><br>
            3. Bisa diberi tambahan nutrisi dengan semprotan pupuk organic cair pada daun dan batangnya, mengimbangi kebutuhan asupan nutrisi.
            <br><br>
            4. Apabila tanaman Anda sudah cukup besar dan kurang muat, sebaiknya langsung dipindah ke pot atau botol yang lebih besar. Berhati-hatilah jangan sampai merusak akar tanamannya.
            <br><br>
            Begitulah cara bercocok tanam hidroponik, mudah untuk pemula. Selain hemat dan bisa diletakkan dimana saja, Anda bisa memasak sayuran dari hasil panen di rumah sendiri. Selamat mencoba dan semoga berhasil.
