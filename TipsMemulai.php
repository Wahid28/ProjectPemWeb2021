            <h1>Memulai sebuah Hidroponik</h1>
            <a href=""><img src="gambar/Hidroponik 1.jpg" alt=""></a>
            <p>
                Cara bercocok tanam hidroponik adalah metode penanaman tanaman tanpa menggunakan media tumbuh dari tanah. Secara harfiah, hidroponik berarti penanaman dalam air yang mengandung campuran hara. Budidaya Tanaman Sayuran dengan Sistem Hidroponik, Balai Penelitian Tanaman Sayuran (Balitsa).
                <br> <br>
                Hidroponik terkenal dengan kemudahannya dalam menanam sayuran. Sebagai pemula yang tidak pernah belajar pertanian sama sekali, Anda tidak perlu khawatir karena hidroponik sangat mudah diaplikasikan oleh siapapun.
            </p>
            <br>
            <h1>
                Cara bercocok tanam hidroponik sistem NFT
            </h1>
            <br>
            <p>
                > Siapkan beberapa pipa atau talang, dan pompa.
                <br><br>
                > Lubangi pipa sesuai dengan panjangnya dengan jarak antar lubang sama.
                <br><br>
                > Susun pipa atau talang yang dipersiapkan untuk jadi tempat menanam tanaman.
                <br><br>
                > Siapkan penampung pada ujung pipa yang lebih rendah.
                <br><br>
                > Pasang pompa untuk mengalirkan air bernutrisi agar alirannya maksimal.
                <br><br>
                > Konsep dasar sistem ini, tanaman tumbuh pada bagian lapisan nutrisi yang tidak dalam, serta menjaga sirkulasi agar tanaman tetap mendapat nutrisi, oksigen, dan air tercukupi.
                <br><br>
                > Cara bercocok tanam hidroponik sistem NFT baik diterapkan untuk sayuran hijau, seperti kangkung dan bayam.
            </p> 
